# When the condition is set via the URL (blinded participant runs), hide the
# researcher control entirely so the participant cannot see or change the version.
url_has_condition = "p" in st.query_params

with st.sidebar:
    st.markdown(f"**Session:** `{st.session_state.session_id[:8]}`")
    if not url_has_condition:
        # Researcher control, shown only for non-blinded runs. For a participant
        # session, open the app with ?p=1 (personalized) or ?p=0 (control).
        choice = st.radio("Version (researcher)", ["A", "B"], horizontal=True,
                          help="A = personalized, B = non-personalized. Set before the session.")
        st.session_state._personalized = (choice == "A")
    if st.button("Start new conversation"):
        st.session_state.session_id = str(uuid.uuid4())
        st.session_state.messages = []
        st.session_state.history = []
        st.session_state.profile = None
        st.session_state.intake_done = False
        st.session_state.consent_given = False
        st.rerun()

personalized = resolve_condition()
condition = "personalized" if personalized else "control"

# Consent gate: shown on open; the bot only proceeds after the user agrees.
if not st.session_state.consent_given:
    render_consent_gate(condition)
    st.stop()

# Gate the chat behind the intake in the personalized condition.
if personalized and not st.session_state.intake_done:
    render_intake_form()
    st.stop()

for msg in st.session_state.messages:
    with st.chat_message(msg["role"]):
        st.markdown(msg["content"])
        if msg.get("sources"):
            st.caption("Sources: " + ", ".join(msg["sources"]))

if question := st.chat_input("Ask a question about osteoarthritis self-management..."):
    st.session_state.messages.append({"role": "user", "content": question})
    with st.chat_message("user"):
        st.markdown(question)

    recent_user_msgs = [m["content"] for m in st.session_state.messages
                        if m["role"] == "user"][-CRISIS_WINDOW_TURNS:]
    signals = detect_signals(question, recent_user_msgs)

    with st.chat_message("assistant"):
        # Safety floor: crisis handling is unconditional and not LLM-generated.
        if signals["crisis"]:
            answer = CRISIS_MESSAGE
            sources = []
            st.markdown(answer)
        else:
            retriever, llm, prompt = get_chain()
            with st.spinner("Looking it up..."):
                docs = retriever.invoke(question)
                if not docs:
                    answer = ("I don't have information on that in my source materials. "
                              "For questions outside these materials, please consult a "
                              "healthcare professional.")
                    sources = []
                else:
                    context = "\n\n".join(d.page_content for d in docs)
                    sources = format_sources(docs)
                    profile_block = build_profile_block(st.session_state.profile) if personalized else ""
                    directives = turn_directives(signals, personalized)
                    messages = prompt.format_messages(
                        profile_block=profile_block,
                        turn_directives=directives,
                        history=st.session_state.history[-HISTORY_TURNS * 2:],
                        context=context,
                        question=question,
                    )
                    try:
                        response = llm.invoke(messages)
                        answer = response.content
                    except Exception as e:
                        answer = f"Sorry, something went wrong: {e}"
                        sources = []
            st.markdown(answer)
            if sources:
                st.caption("Sources: " + ", ".join(sources))

    st.session_state.messages.append({"role": "assistant", "content": answer, "sources": sources})
    st.session_state.history.append(HumanMessage(content=question))
    st.session_state.history.append(AIMessage(content=answer))
    log_turn(st.session_state.session_id, condition, question, sources, answer, signals)