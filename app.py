def detect_signals(text, recent_user_msgs=None):
    """Crisis detection runs over the current message plus the recent user turns
    (to catch multi-turn build-up / conversational drift, Q3); psych and literacy
    are judged on the current message only. Crisis takes precedence."""
    t = text.lower()
    crisis_scope = " ".join(recent_user_msgs).lower() if recent_user_msgs else t
    crisis = any(k in crisis_scope for k in CRISIS_TERMS)
    return {
        "crisis": crisis,
        "psych": (not crisis) and any(k in t for k in PSYCH_TERMS),
        "literacy": any(k in t for k in LITERACY_TERMS),
    }


def turn_directives(signals, personalized):
    """Tiered signal questions (psych + literacy) only fire in the personalized condition."""
    parts = []
    if personalized and signals["psych"]:
        parts.append(
            "\n\nThe user may be experiencing low mood, stress, or worry. Respond with empathy "
            "and gently ask one brief, non-clinical question about how they have been feeling "
            "emotionally lately. Do not diagnose. Encourage talking to their GP or a trusted "
            "person if the feelings persist."
        )
    if personalized and signals["literacy"]:
        parts.append(
            "\n\nThe user appears to find the information hard to follow. Answer in plain, simple "
            "language: short sentences, everyday words, a concrete example, and briefly check "
            "whether it is clear."
        )
    return "".join(parts)


def build_profile_block(p):
    """Render the intake profile into a compact tailoring instruction for the prompt."""
    if not p:
        return ""
    lines = [
        f"- Age {p['age']}, sex {p['sex']}.",
        f"- Weight {p['weight']} kg, height {p['height']} cm (BMI {p['bmi']}).",
        f"- Pain {p['pain_intensity']}/10, {p['pain_nature']}, duration: {p['pain_duration']}.",
        f"- Morning stiffness: {p['morning_stiffness']}.",
        f"- Affected joint: {p['joint']} ({p['joint_count']} joint(s)).",
        f"- Care pathway: {p['care_pathway']}.",
        f"- Work load: {p['work']}; activity level: {p['activity']}.",
    ]
    if p.get("limitations"):
        lines.append(f"- Limitations in daily life: {', '.join(p['limitations'])}.")
    if p.get("comorbidities"):
        lines.append(f"- Comorbidities: {', '.join(p['comorbidities'])}.")
    if p.get("medication"):
        lines.append(f"- Medication: {p['medication']}.")
    if p.get("tried"):
        lines.append(f"- Previously tried: {', '.join(p['tried'])}.")
    if p.get("sport"):
        lines.append(f"- Sport: {p['sport']}.")
    if p.get("goals"):
        lines.append(f"- Goals/preferences: {', '.join(p['goals'])}.")
    return (
        "\n\nThe user has completed an intake. Tailor your advice to the profile below: "
        "weigh relevant factors (e.g. affected joint, pain, weight, comorbidities, goals), "
        "refer to it naturally, do NOT list it back verbatim, and never present tailored "
        "information as a diagnosis. Keep all safety rules above.\nProfile:\n" + "\n".join(lines)
    )


@st.cache_resource
def get_chain():
    """Build retriever, LLM, and prompt once and cache across reruns."""
    load_dotenv()
    if not os.getenv("OPENAI_API_KEY"):
        st.error("OPENAI_API_KEY is not set. Add it to your .env file.")
        st.stop()
    embeddings = OpenAIEmbeddings(model=EMBEDDING_MODEL)
    db = Chroma(persist_directory=DB_PATH, embedding_function=embeddings)
    retriever = db.as_retriever(search_kwargs={"k": TOP_K})
    llm = ChatOpenAI(model=CHAT_MODEL, temperature=0)
    prompt = ChatPromptTemplate.from_messages([
        ("system", SYSTEM_PROMPT + "{profile_block}{turn_directives}"),
        ("placeholder", "{history}"),
        ("human", USER_TEMPLATE),
    ])
    return retriever, llm, prompt


def format_sources(docs):
    seen = []
    for d in docs:
        src = Path(d.metadata.get("source", "unknown")).name
        page = d.metadata.get("page")
        tag = f"{src} p.{page + 1}" if page is not None else src
        if tag not in seen:
            seen.append(tag)
    return seen


def log_turn(session_id, condition, question, sources, answer, signals):
    log_file = LOG_DIR / f"{datetime.utcnow().strftime('%Y-%m-%d')}.jsonl"
    record = {
        "timestamp": datetime.utcnow().isoformat(),
        "session_id": session_id,
        "condition": condition,                 # "personalized" | "control"
        "question": question,
        "sources": sources,
        "answer": answer,
        "signals": signals,                     # crisis / psych / literacy flags
    }
    with log_file.open("a", encoding="utf-8") as f:
        f.write(json.dumps(record) + "\n")


def resolve_condition():
    """Personalized vs control. Researcher sets it; a URL query param ?p=1 / ?p=0
    overrides (useful for blinded runs). Logged per turn."""
    params = st.query_params
    if "p" in params:
        return params["p"] not in ("0", "false", "no")
    return st.session_state.get("_personalized", True)


def render_intake_form():
    st.subheader("Short intake")
    st.caption("A few questions so the assistant can tailor its information to you.")
    with st.form("intake"):
        age = st.number_input("Age", 18, 100, 60)
        sex = st.selectbox("Sex", ["male", "female", "other", "prefer not to say"])
        c1, c2 = st.columns(2)
        weight = c1.number_input("Weight (kg)", 30, 250, 80)
        height = c2.number_input("Height (cm)", 120, 220, 175)
        pain_intensity = st.slider("Pain right now (0 = none, 10 = worst imaginable)", 0, 10, 5)
        pain_nature = st.selectbox("When do you mostly have pain?",
                                   ["mainly with activity", "also at rest", "varies"])
        pain_duration = st.selectbox("How long have you had complaints?",
                                     ["less than 6 weeks", "6 weeks to 3 months", "longer than 3 months"])
        morning_stiffness = st.selectbox("Morning stiffness",
                                         ["none", "less than 30 minutes", "longer than 30 minutes"])
        limitations = st.multiselect(
            "Where do you experience difficulty in daily life?",
            ["climbing stairs", "walking", "getting up/sitting down",
             "getting dressed", "work", "sport", "sleeping"])
        joint = st.selectbox("Which joint has the complaints?", ["knee", "hip", "both"])
        joint_count = st.selectbox("Number of affected joints", ["one", "multiple"])
        comorbidities = st.multiselect(
            "Do you have any of these conditions?",
            ["cardiovascular disease", "diabetes",
             "gastrointestinal problems", "kidney problems"])
        medication = st.text_input("Do you take medication for the complaints? (optional)")
        tried = st.multiselect(
            "What have you already tried?",
            ["exercise therapy/physiotherapy", "painkillers", "injection",
             "weight loss", "nothing yet"])
        care_pathway = st.selectbox(
            "Where are you currently?",
            ["not yet seen a doctor", "under treatment by GP",
             "referred to physiotherapy", "waiting for surgery", "other"])
        work = st.selectbox("What kind of load do you have during the day?",
                            ["mostly sedentary", "mostly standing/walking",
                             "heavy physical work", "not working"])
        activity = st.selectbox("How active are you typically?", ["low", "moderate", "high"])
        sport = st.text_input("Do you play any sports? (optional)")
        goals = st.multiselect(
            "What do you most want to achieve?",
            ["less pain", "better movement", "stay active at work", "be able to play sport",
             "less medication", "postpone surgery"])
        submitted = st.form_submit_button("Start the conversation")
    if submitted:
        bmi = round(weight / ((height / 100) ** 2), 1) if height else None
        st.session_state.profile = {
            "age": age, "sex": sex, "weight": weight, "height": height,
            "bmi": bmi, "pain_intensity": pain_intensity, "pain_nature": pain_nature,
            "pain_duration": pain_duration, "morning_stiffness": morning_stiffness,
            "limitations": limitations, "joint": joint, "joint_count": joint_count,
            "comorbidities": comorbidities, "medication": medication.strip(),
            "tried": tried, "care_pathway": care_pathway, "work": work,
            "activity": activity, "sport": sport.strip(), "goals": goals,
        }
        st.session_state.intake_done = True
        st.rerun()


def log_consent(session_id, condition):
    log_file = LOG_DIR / f"{datetime.utcnow().strftime('%Y-%m-%d')}.jsonl"
    record = {
        "timestamp": datetime.utcnow().isoformat(),
        "session_id": session_id,
        "condition": condition,
        "event": "consent_given",
    }
    with log_file.open("a", encoding="utf-8") as f:
        f.write(json.dumps(record) + "\n")


def render_consent_gate(condition):
    st.markdown(CONSENT_TEXT)
    agree = st.checkbox("I have read the above and I agree to participate.")
    if st.button("Continue"):
        if agree:
            st.session_state.consent_given = True
            log_consent(st.session_state.session_id, condition)
            st.rerun()
        else:
            st.warning("Please tick the box first to continue.")


